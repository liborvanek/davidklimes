                <h2>Nejsou peníze, vracíme se do EBRD</h2>
                <p>
                  Česká republika trochu zpupně vystoupila z Evropské banky pro obnovu a rozvoj v
                  roce 2007, ať se tato finanční instituce zaměřuje na chudší, my jsme už bohatá
                  rozvinutá země. Vše je ale nyní jinak. Na vládu jde žádost z ministerstva financí:
                </p>
                <ul>
                  <li>
                    „S&nbsp;ohledem na výše uvedené se navrhuje rozhodnout o podání oficiální
                    žádosti o obnovení financování investic EBRD na území ČR. Záležitost bude
                    následně projednána Radou ředitelů EBRD. Pro přijetí je nutné schválení dvojí
                    většinou, která představuje 2/3 všech akcionářů a 3/4 hlasovací síly. Většina
                    akcionářů EBRD již na pracovní úrovni předběžně vyjádřila podporu ČR.“
                  </li>
                </ul>
                <p>
                  O co jde: V&nbsp;současné době EBRD soustředí veškerou svou činnost na pomoc zemím
                  operací s ekonomickými dopady pandemie COVID-19, na což oficiálně vyhradila 21
                  miliard EUR do konce roku 2021 s&nbsp;důrazem na podporu reálné ekonomiky a rozvoj
                  infrastruktury. Česká vláda by se ráda na tyto peníze také sáhla. V listopadu už
                  tady byla mise banky, aby si vytipovala projekty.
                </p>
                <ul>
                  <li>
                    „Analýza EBRD předpokládá cílené a časově omezené opětovné zapojení EBRD
                    v&nbsp;ČR. Za prioritní oblasti pro nové operace byly vyhodnoceny: i) zelené
                    investice, obnovitelné zdroje a infrastruktura; ii) digitalizace a nové
                    technologie (ICT); a iii) finanční sektor. Je zřejmé, že EBRD se v případě
                    obnovení financování na území ČR bude primárně zaměřovat na soukromý sektor.“
                  </li>
                </ul>
                <p>
                  V ČR by dle dosavadních odhadů EBRD mohly investice dosahovat minimálně 100-200
                  milionů eur ročně. Obnovení operací by bylo pouze dočasné a financování nových
                  projektů by bylo omezeno na dobu maximálně 3-5 let. Celkově lze tedy očekávat
                  dodatečné investice ve výši až miliardy eur.
