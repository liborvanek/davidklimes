                        <div>
                          <h2>Místo činu: rekonstrukce volebního dramatu u Ústavního soudu</h2>
                          <p>
                            Ostrých komentářů k <a
                              href="https://www.usoud.cz/fileadmin/user_upload/Tiskova_mluvci/Publikovane_nalezy/2021/Pl._US_44_17_vcetne_disentu.pdf"
                              >zásadnímu nálezu Ústavního soudu</a>
                            vyšlo více než dost (<a
                              href="https://nazory.aktualne.cz/komentare/nejnicivejsi-nalez-ustavniho-soudu-covidovy-chaos-prebijime/r~e50cae36666211eb8b230cc47ab5f122/"
                              >jeden jsem také přidal</a
                            >). Ale dosud vlastně nevíme, co a proč se vlastně stalo. Týden před
                            vyhlášením nálezu nikdo včetně mnoha ústavních právníků netipoval, že v
                            Brně vyhlásí něco tak ničivého, co navíc otáčí předchozí judikaturu o
                            180 stupňů.
                          </p>
                          <p>
                            Z různých zdrojů se mi podařilo sestavit rekonstrukci, co se vlastně na
                            Ústavním soudu událo a proč. Nijak si to nezadá s kriminálkou Místo činu
                            – je to napínavé a občas také dost béčkové...
                          </p>
                          <p />
                          <p><strong>Expozice: Fenyk povrzuje potvrzené</strong></p>
                          <p>
                            V souvislosti s volebním zemětřesením se zmiňují všemožné předchozí
                            nálezy, především pak 42/2000, ale ten zásadní pro naši detektivku nese <a
                              href="https://nalus.usoud.cz/Search/GetText.aspx?sz=Pl-42-17_2"
                              >číslo 42/2017</a
                            >.
                          </p>
                          <p>
                            Naprosto zapomenutý případ, který byl tak praštěný, že se ani nedostal
                            do médií. Jistý Milan Marko si stěžoval na volby v roce 2017, a to velmi
                            hloupým způsobem, kdy napadal ne volební pravidla, ale Nejvyšší správní
                            soud za potvrzení&nbsp;výsledků. Teprve k této bizarnosti připojil
                            stížnost i proti pravidlům, které křiví poměrné zastoupení při volbách
                            do sněmovny (aby toho nebylo málo, ještě to okořenil atakem na soudce
                            Musila).
                          </p>
                          <p>
                            Věc měl na starost soudce zpravodaj Fenyk a v dubnu 2018 dospěl s
                            drtivou většinou kolegů k nijak překvapivému nálezu. Celé je to pitomost
                            a na volební paragrafy se sahat nebude, protože to už v minulosti
                            Ústavní soud celkem jasně vysvětlil:
                          </p>
                          <blockquote>
                            <p>
                              K otázce ústavní přijatelnosti právní úpravy přepočtu hlasů na mandáty
                              vyjádřil již opakovaně a poukázal na usnesení sp. zn. Vol 50/2006 a
                              uvedl, že použití d´Hondtovy metody ve volbách do Poslanecké sněmovny
                              bylo několikrát označeno za souladné s ústavním pořádkem i Ústavním
                              soudem.
                            </p>
                          </blockquote>
                          <p>
                            A dál Fenyk s odkazem na zavedenou judikaturu rozcupoval stěžovatelovy
                            argumenty jeden za druhým.
                          </p>
                          <p>
                            Všichni další soudci byli pro, konec konců jiný názor by popřel vše, co
                            dosud ve věci ÚS říkal. Pavel Rychetský, který sám kdysi pro Zemana a
                            Klause většinové prvky vymýšlel, bez problému připojil podpis a bylo
                            hotovo.
                          </p>
                          <p />
                          <p><strong>Kolize: Šimáčková ostře disentuje</strong></p>
                          <p>
                            Jenže k nálezu byl připojen i jeden ostrý disent soudkyně Šimáčkové
                            (ostrý i na její poměry). V krátkosti: uznává, že stěžovatel asi nemá
                            všech pět pohromadě a jeho návrh není možné přijmout, ale s potvrzením
                            volebních pravidel nemůže souhlasit, protože „právní úprava voleb do
                            Poslanecké sněmovny Parlamentu ČR napadená posuzovaným návrhem je v
                            rozporu s ústavním pořádkem České republiky“.
                          </p>
                          <p>
                            A dále řadila svých šest námitek, které nyní po aktuálním nálezu musí
                            přijít velmi povědomé.
                          </p>
                          <blockquote>
                            <p>
                              Omezení svobodné soutěže politických sil a stran (zaručené čl. 5
                              Ústavy a čl. 22 Listiny) a poměrnosti voleb do Poslanecké sněmovny
                              (zaručené čl. 18 odst. 1 Ústavy) celostátní pětiprocentní uzavírací
                              klauzulí.
                            </p>
                          </blockquote>
                          <blockquote>
                            <p>
                              Omezení svobodné soutěže politických sil a stran, rovnosti hlasů
                              (zaručené čl. 18 odst. 1 Ústavy a čl. 21 odst. 3 Listiny) a poměrnosti
                              voleb do Poslanecké sněmovny přirozeným prahem, který ve dvou malých
                              volebních obvodech (Karlovarském a Libereckém kraji) pravidelně
                              přesahuje 10 % - takto vysoký přirozený práh v malých volebních
                              obvodech kombinovaný s klasickým výpočtem D'Hontovým dělitelem (aniž
                              by bylo zabráněno propadnutí hlasů odevzdaných v těchto krajích,
                              například pomocí druhého skrutinia na celorepublikové úrovni) je
                              zásahem do poměrnosti voleb i rovnosti váhy voličských hlasů.
                            </p>
                          </blockquote>
                          <ul>
                            <li>
                              Organizace voleb na krajském základě (respektováním teritoriálního
                              principu reprezentace podle krajského zřízení) s přítomností dvou
                              malých obvodů, při současném bezdůvodném ponechání umělé pětiprocentní
                              uzavírací klauzule na celostátní úrovni.
                            </li>
                            <li>
                              Psychologický a mediální rozměr součtu působení umělé celostátní
                              pětiprocentní uzavírací klauzule a současně více než desetiprocentního
                              přirozeného prahu pro získání mandátu v malých krajích a odraz této
                              reality na svobodnou soutěž politických stran a sil, ochranu politické
                              menšiny (zaručenou čl. 6 Ústavy) a rovnost hodnoty hlasů voličů malých
                              i velkých stran.
                            </li>
                            <li>
                              Aditivní uzavírací klauzule bránící efektivnímu uzavírání
                              předvolebních koalic - opět jako zásah do svobodné soutěže politických
                              stran a sil, ochrany politické menšiny a rovnosti hodnoty hlasů voličů
                              velkých a malých stran.
                            </li>
                            <li>
                              Nová úprava ztěžující soudní přezkum voleb je vážným omezením
                              volebního práva jednotlivce a jeho soudní ochrany, zaručeného čl. 21 a
                              čl. 36 Listiny.
                            </li>
                          </ul>
                          <p>
                            K tomu dodala, že jednotlivé většinové prvky nejsou problém, ale jejich
                            kombinace je toxická. A celkem nepokrytě naznačovala, že by chtěla
                            alespoň druhé skrutinium jako bolestné pro malé strany.
                          </p>
                          <p />
                          <p><strong>Krize: Filipovi se zalíbil armageddon</strong></p>
                          <p>
                            Jenže stížností na volby bylo více a jakmile tato padla, byla tu ta od
                            senátorů, kterou dostal soudce Filip. Ten se do toho zcela ponořil, v
                            roce 2018 si většina myslela, jestli tam přeci jen nehledá ještě nějakou
                            drobnost na jinak jasné věci, co by oproti Fenykovu názoru mohl
                            škrtnout. V roce 2019 už všichni jsou dost nervózní, ale dobře, profesor
                            musí dodělávat ještě lex Babiš. V roce 2020 již všichni šíleli:
                            přestával&nbsp;pomalu být dobrý čas na vynesení čehokoliv k blížím se
                            volbám. Také už není tajemství, že zcela minoritní názor kolegyně
                            Šimáčkové profesora schvátil a ten se rozhodl pro velký třesk – to
                            znamená kompletně volební pravidla shodit.
                          </p>
                          <p>
                            Z textu nálezu, který se pak v rychlosti předělával, je to místy stále
                            dost patrné. Zdlouhavé výlety do všemožných ústav a všech možných
                            století ve všech světových jazycích mají jediný cíl – zpochybnit vedle
                            přepočtu i volební kraje. Např.:
                          </p>
                          <ul>
                            <li>
                              „Rozhodující pro posouzení je, jak odpovídají napadená ustanovení ZVP
                              podstatě poměrného zastoupení. Tou je myšlenka, že právo na zastoupení
                              v parlamentu jako celostátním reprezentativním orgánu [ne pouze ve
                              volebním kraji] má každá skupina voličů v poměru, který odpovídá
                              velikosti jejího podílu na celkovém počtu voličů. Přitom tato skupina
                              voličů se identifikuje v rámci celého státu, nikoli jako regionální
                              reprezentace.“
                            </li>
                          </ul>
                          <p>
                            Profesor zručně ohýbá historii, aby vydestiloval jakousi ponornou řeku
                            superčistého poměrného systému v českých zemích. S obdivem cituje
                            prvorepublikového Alfréda Meissnera: „Zůstali jsme na principu naprosto
                            spravedlivém, na principu poměrného zastoupení menšin. Jak jednotlivé
                            vrstvy a národnosti v republice jsou zastoupeny, tak mají býti
                            zastoupeny také v zákonodárných sborech při projednávání ústavní
                            listiny“. Ovšem už ve své honbě za krystalickým poměrným systémem vůbec
                            neřeší další dobové integrační prvky jako vázané kandidátky a podobně,
                            volby 1935, kdy nejvíce mandátů dostane ten, kdo nezíská nejvíce hlasů,
                            samozřejmě nezmiňuje. Praxe nesmí kazit teorii.&nbsp;
                          </p>
                          <p>
                            Bod 116 je pravděpodobně ten, kdy se na starou kostru ústavní zbraně
                            volebního ničení začalo psát něco úplně jiného. Takže profesor už jen
                            připojil povzdech:
                          </p>
                          <ul>
                            <li>
                              „Pro ‚ideální’&nbsp;řešení (taková však v praxi neexistují) by bylo
                              třeba začít znova, tedy zrušením ZVP jako celku a rozhodnutí
                              prezidenta republiky č. 611/2020 Sb., o vyhlášení voleb do Poslanecké
                              sněmovny Parlamentu České republiky.“
                            </li>
                          </ul>
                          <p>
                            Co se jen mohlo stát, aby profesor pečlivě chystané rozlomení sedmé
                            pečetě vzdal?
                          </p>
                          <p />
                          <p><strong>Peripetie: Tragická indiskrece Zemanovi</strong></p>
                          <p>
                            Že Filip chystá převrátit volební pravidla, už koncem roku 2020 Brno
                            vědělo. Podstatné je, že se to nemělo za žádnou cenu dostat do Prahy
                            exekutivě, aby nemohla ve zlé vůli zasáhnout.
                          </p>
                          <p>
                            Jenže to se přesně stalo. Prezident Zeman nevyhlásil extrémně brzo volby
                            kvůli nějakým 90 milionům na kampaň pro opoziční koalice (jak si spousta
                            lidí včetně mě myslelo), ale protože se právě dozvěděl, co se na
                            Ústavním soudu chystá.
                          </p>
                          <p>
                            Reputace Ústavního soudu je touto havárií už dost poškozena, takže není
                            nutné zmiňovat, kdo to vynesl. Ale stačí si vzpomenout, kdo naposledy
                            dělal k volebním věcem potvrzující nález, nyní je mezi disentujícími,
                            schází se s Vratislavem Mynářem a dělal náměstka Marii Benešové...
                          </p>
                          <p />
                          <p><strong>Katastrofa: Fiat iustitia, et pereat mundus</strong></p>
                          <p>
                            Jakmile Zeman vyhlásil termín, začala katastrofa. Profesor už neměl
                            šanci tam dopisovat další údaje o bádenské či dánské ústavě a popisovat
                            další historické nesouvislé peripetie. Bylo třeba se rychle rozhodnout.
                            Že by je prezident přetlačil, to nepřicházelo v úvahu. Stejně tak se ale
                            už osm měsíců před volbami nenašla většina pro totální armageddon.
                          </p>
                          <p>Takže se začal dopisovat do nálezu úplně náhle jiný příběh:</p>
                          <ul>
                            <li>
                              „Své funkci soudního orgánu ochrany ústavnosti podle čl. 83 Ústavy zde
                              mohl dostát vzhledem k tomu, co bylo právě uvedeno, i tak, že
                              ústavnost jednotlivých prvků volebního systému bude hodnocena z
                              hlediska praktické potřeby jednoho „pevného bodu“ v tomto systému, s
                              jehož pomocí lze ‚pohnout’&nbsp;zbývajícími prvky a celým volebním
                              systémem tak, aby byla napravena jemu vytýkaná neústavnost.“
                            </li>
                          </ul>
                          <p>
                            Pevným bodem se nepochopitelně staly nevyhovující volební kraje, tedy
                            to, co je na spoustě stran předchozího textu právě zpochybňováno jako
                            výrazná překážka pro poměrnost. Soudci toužící po poměrnosti tak
                            absurdně nechali v systému velmi nestejné obvody, kde na Karlovaráky při
                            jakémkoliv přepočtu vždy zbude pět mandátů pro velké strany a ani
                            případné druhé skrutinium voliče menších stran nikdy zcela neuspokojí.
                          </p>
                          <p>
                            Co zjevně mělo být odstřeleno jako celek, musel najednou profesor
                            výběrově a zcela nelogicky rozstřílet (přepočet a aditivní kvóra fuj,
                            pětiprocentní klauzule a kraje fajn). Podle toho to taky vypadá,
                            najednou se jak mávnutím kouzelného proutku ztrácí opulentní ohromování
                            německými, anglickými a francouzskými citacemi a začíná zoufalá snaha
                            udržet nějakou logickou návaznost.
                          </p>
                          <p>
                            Celý text bojuje profesor Filip o to, aby i ten nejposlednější hlas měl
                            svoji poměrnou reprezentaci, ale v případě pětiprocentní klauzule je
                            najednou úplně v pohodě, když propadne skoro pětina hlasů: „Jak ukazují
                            výsledky voleb za posledních dvacet let, počet propadlých hlasů nikdy
                            nepřekročil hranici jedné pětiny, když jde o počty v roce 1998 – 11,32
                            %, 2002 – 12,53 %, 2006 – 5,97 %, 2010 – 18,83 %, 2013 – 12,58 % a v
                            roce 2017 – 6,29 %.“ A za chvilku je to zase jinak, když u koalic je
                            třeba zrušit aditivní kvóra, protože by tak mohlo propadnou třeba i 18
                            procent hlasů a je jedno, že se to nikdy dosud nestalo.
                          </p>
                          <p>
                            Pokud bych z obsáhlého nálezu měl vypíchnout jedinou větu, tak asi
                            závěrečné shrnutí:
                          </p>
                          <ul>
                            <li>
                              „Východiskem pro to byl výklad pojmu ‚zásady poměrného
                              zastoupení’&nbsp;(čl. 18 Pl. ÚS 44/17 58 odst. 1 Ústavy) rovněž za
                              pomoci okolností jejich vzniku a představ ústavodárce (a současně
                              zákonodárce) v letech 1920, 1946 a 1992.“
                            </li>
                          </ul>
                          <p>
                            I kdyby se práce profesora Filipa tak netáhla a nakonec tak moc
                            nezpolitizovala, nikdy nemohla skončit dobře. Hledání deklarované zásady
                            poměrného zastoupení bez pochopení praktických dostředivých prvků
                            tehdejší dobové politiky (přidání&nbsp;jen částečně
                            svobodných&nbsp;voleb&nbsp;1946 je samo&nbsp;o sobě absurdní) nikdy
                            nemohlo skončit jinak, než hodně vyoseným teoretickým esejem, který
                            navíc bude mít svým časováním, popřením předchozí judikatury a absencí
                            přechodných omezení možná ještě hodně zlé systémové následky a otřese
                            stabilitou demokratického právního státu.<br /><br /><em
                              ><strong>#ToPodstatné</strong>: Jak měl podle vás - právníků i
